package coreJavaTraining;

public class ForLoopOnlyExample {

	public static void main(String[] args) {
		
//		for (int means Initialization ; Condition ; Increment)
// ++ mean increment by 1, -- decrease by 1		
		for (int i=0; i<=100; i=i+20)
		{
			System.out.println(i);
		}
		
		for (int i=0; i<=5; i++)
		{
			System.out.println(i);
		}
		
		for (int i=5; i<=5; i++)
		{
			System.out.println(i);
		}


	}

}
